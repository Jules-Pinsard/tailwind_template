# After bundle
after_bundle do

  # Generators: db + simple form + pages controller
  rails_command "db:drop db:create db:migrate"
  generate("simple_form:install")
  generate(:controller, "pages", "home", "--skip-routes", "--no-test-framework")

  # Routes
  # route 'root to: "pages#home"'
  gsub_file(
    "config/routes.rb",
    '# root "posts#index"',
    'root to: "pages#home"'
  )

  # Gitignore
  append_file ".gitignore", <<~TXT
    # Ignore Mac and Linux file system files
    *.swp
    .DS_Store
  TXT

  # Devise install + user
  generate("devise:install")
  generate("devise", "User")

  # Application controller
  run "rm app/controllers/application_controller.rb"
  file "app/controllers/application_controller.rb", <<~RUBY
    class ApplicationController < ActionController::Base
      before_action :authenticate_user!
    end
  RUBY

  # migrate + generate, update, and style devise views
  rails_command "db:migrate"
  generate("devise:views")
  gsub_file(
    "app/views/devise/registrations/new.html.erb",
    "<%= simple_form_for(resource, as: resource_name, url: registration_path(resource_name)) do |f| %>",
    "<%= simple_form_for(resource, as: resource_name, url: registration_path(resource_name), data: { turbo: :false }) do |f| %>"
  )

  gsub_file(
    "app/views/devise/sessions/new.html.erb",
    "<%= simple_form_for(resource, as: resource_name, url: session_path(resource_name)) do |f| %>",
    "<%= simple_form_for(resource, as: resource_name, url: session_path(resource_name), data: { turbo: :false }) do |f| %>"
  )

  # Pages Controller
  run "rm app/controllers/pages_controller.rb"
  file "app/controllers/pages_controller.rb", <<~RUBY
    class PagesController < ApplicationController
      skip_before_action :authenticate_user!, only: [ :home ]

      def home
      end
    end
  RUBY

  # Create Simple form config file
  run "curl -L https://raw.githubusercontent.com/Jules-Pinsard/tailwind_template/master/simple_from_flowbite.rb > config/initializers/simple_form_flowbite.rb"

  #Install Flowbite via CDN
  inject_into_file "app/views/layouts/application.html.erb", before: '<%= stylesheet_link_tag "application", "data-turbo-track": "reload" %>' do
    <<~HTML
      <script src="https://cdnjs.cloudflare.com/ajax/libs/flowbite/2.2.1/flowbite.turbo.min.js"></script>
    HTML
  end

  # Environments
  environment 'config.action_mailer.default_url_options = { host: "http://localhost:3000" }', env: "development"
  environment 'config.action_mailer.default_url_options = { host: "http://TODO_PUT_YOUR_DOMAIN_HERE" }', env: "production"

  # Dotenv
  run "touch '.env'"

  # Rubocop
  run "curl -L https://raw.githubusercontent.com/Jules-Pinsard/tailwind_template/master/.rubocop.yml > .rubocop.yml"

  # Git
  git :init
  git add: "."
  git commit: "-m 'Rails new with https://raw.githubusercontent.com/Jules-Pinsard/tailwind_template/master/template.rb (Tailwind forms)'"
end
